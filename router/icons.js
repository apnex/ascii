#!/usr/bin/env node

const sym = {
	thin: [
		'\u{0020}', // ' '
		'\u{256D}', // '╭'
		'\u{256E}', // '╮'
		'\u{256F}', // '╯'
		'\u{2570}', // '╰'
		'\u{2500}', // '─'
		'\u{2502}'  // '│'
	],
	thick: [
		'\u{0020}', // ' '
		'\u{250F}', // '┏'
		'\u{2513}', // '┓'
		'\u{251B}', // '┛'
		'\u{2517}', // '┗'
		'\u{2501}', // '━'
		'\u{2503}'  // '┃'
	],
	port: [
		'\u{252B}', // '┫'
		'\u{2523}', // '┣'
		'\u{253B}', // '┻'
		'\u{2533}'  // '┳'
	]
};

var square = [
	[1,5,5,5,2],
	[6,0,0,0,6],
	[4,5,5,5,3]
];

square.forEach((row) => {
	var string = "";
	row.forEach((char) => {
		string += sym.thick[char];
	});
	console.log(string);
});
